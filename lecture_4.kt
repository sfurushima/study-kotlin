fun main() {

    /*
    * ・画面出力
    * ・四則演算
    * ・文字列結合
    * */

    // 改行なし
    print("Hello")

    // 改行あり
    println("World")

    // 改行コード "\n"
    println("HELLO\nWORLD")

    // 数値
    println(10)

    // 文字列：ダブルコーテーションで囲む
    println("100")

    // 算術演算子　+, -, *, /, %
    println(10 + 2)
    println(10 - 2)
    println(10 * 2)
    println(10 / 2)
    println(10 % 3)

    // 文字列結合
    println("HELLO" + "WORLD")

}